                <h1>Como escolher um bom roteador?</h1>
    
        
<p>Muitas pessoas caem no erro de comprá-lo, sem antes sequer descobrir se realmente necessita ter um roteador em casa. Para isso, você deve saber qual a função do dispositivo e como ele pode ser utilizado. Basicamente, a principal função do produto é criar uma rede entre computadores, mas também pode ser utilizado para aumentar a segurança da sua conexão.

Além disso, o dispositivo disponibiliza ao usuário a possibilidade de criar uma rede Wi-Fi domestica ou até mesmo empresarial, para ser utilizada em notebooks, smartphones, tablets, consoles, entre muitos outros aparelhos que suportam uma das tecnologias mais utilizada, a “Wireless” (Internet sem fio).</p>


<h2>Qual modelo comprar?</h2>

<p>Antes de escolher qual o tipo de roteador adquirir, é preciso levar em consideração alguns quesitos importantes para que você tenha um aparelho que comporte a internet contratada sem haver nenhum problema. Primeiramente, é preciso identificar o tipo de conexão utilizada. Se você possui uma banda larga fixa (ADSL ou Cabo), um roteador comum funciona corretamente.</p>

<p>Agora, se por acaso a conexão for 3G, o usuário terá que procurar por um roteador que possibilita a instalação interna do chip da operadora de internet ou que tenha uma conexão USB embutida para que permita a utilização do modem.</p>


<p>Além do mais, você terá que buscar por um aparelho compatível com a velocidade da sua internet. Existe alguns modelos de roteadores disponíveis no mercado, entre eles os mais comuns são: o padrão 802.11b (suporta até 11 Mbps), o 802.11g (até 54 Mbps), 802.11n (chegando até 450 Mbps) e o padrão mais atual 802.11ac (fornece velocidade superior a 1Gbps), embora ainda não seja muito utilizado.</p>

<h2>Quantidade de Antenas</h2>
<p>Uma das maiores confusões que alguns usuários fazem, é relacionado as antenas que o aparelho contem. Embora a maioria dos roteadores possuem pelo menos uma antena, mesmo que não estejam visíveis, você deve prestar atenção no tamanho do lugar em que a rede será criada e se ela possui barreiras, como paredes ou algo do tipo. Se esse for o caso, é uma ótima opção comprar um roteador que comporte pelo menos uma ou duas antenas externas ou utilizar um repetidor, para que dificilmente ocorra interferências nos sinais.</p>

<h2>Segurança</h2>
<p>Para evitar futuras dores de cabeça relacionadas a invasões de criminosos e outros problemas de segurança, é altamente recomendado que você invista em aparelhos que possuam Firewall integrado.</p>

<p>Para isso, procure por roteadores que utilizam os sistemas de Firewall SPI e encriptações WPA e WPA2, que ultimamente são os mais recomendados. Diante disso, você poderá criar uma barreira para dificultar muito a vida de quem tentar invadir a sua rede com a intenção de roubar informações.</p>

<h2>Frequência</h2>
<p>Atualmente, é muito comum encontrar nas lojas roteadores que trabalham com frequência semelhante a 2,4 GHz. Porém, em tese, isso pode ser um enorme problema para as pessoas que moram em prédios que possuam diversas redes Wi-Fi, pois, ocorrera uma grande interferência no sinal e queda no desempenho da internet.</p>

<p>Para resolver esse problema escolha um roteador que trabalhe com duas frequências, a 2,4 GHz e 5 Ghz. No entanto, será necessário um maior investimento para que você não seja prejudicado por perca de sinal e eficiência.</p>


